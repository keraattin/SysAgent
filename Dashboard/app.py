#!/usr/bin/env python3

# Libraries
##############################################################################
from flask import Flask
##############################################################################

# Configs
##############################################################################
HOST = '0.0.0.0'
PORT = 5000

app = Flask(__name__)
##############################################################################

# Routes
##############################################################################
@app.route('/')
def hello_world():
    return 'Hello, World!'
##############################################################################

# Main
##############################################################################
if __name__ == '__main__':
    app.run(host=HOST, port=PORT, debug=True)
##############################################################################